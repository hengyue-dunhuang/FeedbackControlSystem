void set_motor(int rot, uint8_t speed) //0-200 ->  50-1050
{
	int duty = speed*5+40; //dead zone
	if(rot==1)
	{
	
	HAL_GPIO_WritePin(GPIOF,GPIO_PIN_9,1);
	HAL_GPIO_WritePin(GPIOG,GPIO_PIN_9,0);
	__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3,duty);
	}
	else if(rot==0)
	{
		
	HAL_GPIO_WritePin(GPIOF,GPIO_PIN_9,0);
	HAL_GPIO_WritePin(GPIOG,GPIO_PIN_9,1);
		__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3,duty);
	}
	
}

